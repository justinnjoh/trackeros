    <h4>
      Simple Tracker
    </h4>

    <span class="text-muted">
      Simple tracker is a simple issue tracking system that can also be used as a blogging or gallery application.
      It is aimed at those who, like the author, need an easy system to record, prioritise, assign and resolve issues without the complexities of many of the systems out there.
    </span>

    <span class="block m-t">
      Simple Tracker is aimed primarily at people with a good level of programming knowledge. This is by no means a finished piece of work. Please make improvements for your own use or better still, for sharing.
